import sqlite3
import re
from datetime import datetime, timedelta

DB_PATH = 'vermillon.db'

def get_db():
    return sqlite3.connect(DB_PATH)

def insert_memo(content, created_at, mood=''):
    db = get_db()
    cursor = db.cursor()
    
    # title extraction
    lines = content.strip().splitlines()
    title = None
    for line in lines:
        stripped = line.strip()
        if stripped.startswith('# ') or stripped.startswith('## '):
            title = stripped.lstrip('#').strip()
            break
    if not title:
        text = content.strip().replace('\n', ' ')[:20]
        if len(text) == 20:
            text += '...'
        title = text or '(无标题)'
    
    cursor.execute(
        'INSERT INTO memos (content, title, mood, created_at, updated_at) VALUES (?, ?, ?, ?, ?)',
        (content, title, mood, created_at, created_at)
    )
    memo_id = cursor.lastrowid
    
    tags = list(set(re.findall(r'#([\w\u4e00-\u9fa5_/]+)', content)))
    for tag in tags:
        cursor.execute('INSERT OR IGNORE INTO tags (name) VALUES (?)', (tag,))
        cursor.execute('SELECT id FROM tags WHERE name = ?', (tag,))
        tag_id = cursor.fetchone()[0]
        cursor.execute('INSERT INTO memo_tags (memo_id, tag_id) VALUES (?, ?)', (memo_id, tag_id))
    
    db.commit()
    db.close()
    return memo_id

